２週目は長い文を理解しながら読んで頂き、人に伝わる音読のリズム感の練習に挑戦します。 <br>
ご自身の声を録音したものを送って下さると担当講師が速度、リズム、流暢性の３分野をチェックし、的確なアドバイスを致します。 ネイティブ講師の音読録音はゆっくりバージョンとナチュラルスピードバージョンの２つをご提供致します。
<br>
