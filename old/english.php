	<p align="left">﻿
 テキストを使って、聞く・話す・読む・書くを総合的に学習します。<br />
文法、語彙、表現についての解説部分は講師が率先してしゃべりますが、レッスンの7割は音読や会話で生徒さんにどんどん声を出していただくことをモットーにしております。<br />
それは、声に出してこそ「話すチカラ」がつき、それが「聞くチカラ」「読むチカラ」の向上へとつながるからです。<br />通訳が行っている勉強法であるオーバーラッピングやシャドーイングも行います。<br />
丁寧な解説で文法についてのポイントを学んだ後は、練習問題で反復練習し「書くチカラ」も身につけます。<br />
そして、学習したことを定着させるため、復習は欠かせません。授業中に学習した本文や例文をすべて暗記していただいて次のレッスン時に、すべて英語で声に出していただきます。<br />
このように丁寧に進めるため、レッスンの進度は1コマ（55分）で１つの課の５０％～８０％ほどが目安です。<br />
