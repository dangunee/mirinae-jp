マのセリフがよく聞こえるようになりました。さらに前は文章を読むのが苦痛でしたが、

今は楽になり、いろいろな文章を読むのがとても楽しいです。それも皆この講座の幅広い

内容のおかげだと思っています。１回４時間のちょっときつい講座ですが、それだけ内容

