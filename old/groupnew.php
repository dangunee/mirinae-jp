どんな内容かというと…<br>

始まるとまず順番に近況報告をします。普通、近況報告は勉強じゃないと思うでしょう

？ところがこれが、もうすでに勉強なのです。それを聞きながら先生が一人一人の表現を

直してくれたり、関連する単語や表現を教えてくれたりするのです。楽しくてためになり

ます。<br>

ちょっと気持ちがほぐれたところでテキストに使っている「韓国語トレーニングブック

」の学習をします。１回に大体１課ずつ進みます。最初の頃は練習問題を全部やっていまし

たが、最近はその課の文法の説明と単作文を中心にやっています。普段からあいまいだっ

た文法や表現の微妙なニュアンスの違いなどを質問すると、必ず納得できる答えが聞ける

ところがすごいところです。<br>

それから、A４で４ページ分くらいのちょっと長い文章を読みます。毎回テーマが違っ

ているので、語彙を増やすのに役立っています。前回は韓国の現代史に関する文章だった

ので、読む前に先生がざっと現代史を講義してくださいました。こんな講義を聞けるのも

この講座のよいところです。歴史を知らないといけないのはわかっていてもなかなか手を

つけられなかった私ですが、この日帰ってから韓国の現代史をネットで検索しました。先

生が説明してくださったのをメモしてきたノートを見ながら、ネットで読んでいると今ま

で頭に入らなかった内容もすうっと入ってきて分かりやすかったです。歴史や文化を知る

ことも韓国語を勉強する私たちにとって大切なことだと思います。<br>

最後に宿題のパダスギの答え合わせをします。パダスギは１５分のラジオ番組の音声デ

ータを送ってくださるので、それを全部書き取って持っていくと、その場で解答をしなが

ら、内容について話し合ったりします。聞き取ることはできてもいざ書こうとすると、単

語の最後がㄴだったのかㅇだったのか分からなかったりすることが多くて辞書を引いては

確認しています。聞き取りの練習にもなるし、あいまいな맞춤법の確認もできて一石二鳥

なのがこのパダスギです。<br>

以上が土曜講座の内容です。どうですか？実に多岐に渡る内容ですね。前はこれに昔話

の聞き取りもあったのです。昔話は子供に話しかける語り口調で、擬態語や擬声語が多く

、漢字語でない固有の韓国語で語られていて、これもなかなかよかったです。<br>

さて土曜講座の効果はどうかというと…<br>

