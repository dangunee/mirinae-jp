			 会話のレベルは、単にたくさんしゃべることだけでは伸びません。知ってる表現だけではなく、より多くの正確な表現を使う練習が必要です。
			 ミリネ韓国語教室では皆さんが、より多くの語彙と的確な文型を使って、様々な分野の会話が出来るように「会話トレーニングレッスン」を開講しております。
			 ミリネ独自のテキストを利用し、毎回違うテーマについて指定された文型を使って作文・添削・繰り返し話す練習をしながら文型を身につけます。
			 </p>
			 <hr>
             
			 <p align="left"><font  color="#B50003"><mark>➊　ミリネ独自のテキストを使用</mark></font><br>
			事前にテキストをお渡ししますので、指定された文型で作文をしてきてください。添削・文型を使った会話の練習をします。</p>
		            
			<p align="left"><font  color="#B50003"><mark>➋　単なるお喋りとは違う！</mark></font><br>
			事前に作文を書いていただきます。授業中に作文の間違いを確認することで似ている文型との違いを学びます。多様な文型を使って、自ら作文して話すことで、会話のレベルが上がります。</p>
		    
