  反復的な学習を通じて体に馴染ませていくのが何より重要だと言えます。 しかし、反復学習にもかかわらず、伸びない理由は何でしょうか。<br>
  多くの学習者たちの共通の悩みは、単語や表現がよく覚えられないこと、そして覚えても忘れてしまい、会話まで繋いでいけないことです。 <br>
