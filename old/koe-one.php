今は尊敬語です。<br><br>

 
<b>1級を終えてのご感想は？</b><br>
１冊を終えるまで自分が続けるのか分からなかったので、今はすごい達成感があります。あと、作文がちょっとできるようになったかなと思いました。<br><br>
 
<b>韓国語の勉強を続けられた原動力は何でしたか。</b><br>
それは先生たちのおかげではないでしょうか。授業がすごく楽しかったからです。
いつもワンパターンじゃないというかいろんな話ができるし、教科書だけではなく話とかで楽しかったです。
 <br><br>
<b>他の言語の勉強もしましたか。</b><br>
英語を何度かやって挫折しました。英語だと何で楽しくないんだろう。「できないとだめ」という義務感がありました。
韓国語は趣味で習うものなのでできなくても誰も文句言わないし、楽しくできました。
