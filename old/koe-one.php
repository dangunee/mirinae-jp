 <br><br>
<b>今後の目標は？</b><br>
まだちょっと書けるぐらいになっただけなので、しゃべれるようになりたいです。
書くのは時間をかけて単語を調べればできるものですが、しゃべるのはとっさに出ないといけないものですから。韓国語の勉強は今後も続けます。頑張ります。
