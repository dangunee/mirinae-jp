</b>
最初はすごく戸惑ったんですけど、でも先生も日本語がすごく上手だし、気がついたのは日本人に韓国語の文法について日本語で説明されても自分が学んできた過程で教えられるから難しいと思いますね。<br><br>

それが、韓国人の先生だととても分かりやすく説明してもらえるし、文法的なことも分かりやすかったし、先生の人柄がいいなと思ったので、そのあとにも通わせてもらおうかなと思いました。<br><br>

<b>ミリネでの授業は予習と復習の量が多いと思いますけれども、大変ではありませんか。</b>
<br>
さほど、最近は大変じゃないですね。最初は勉強の仕方自体が分からなくて、大変な感じはありましたけど、今までの一週間の一回90分のペースでは、さほど大変ではなくなってきました。
<br><br>
<b>短期集中レッスンは一週間に3時間のレッスンを受けなければならないのですが、どうですか。</b><br>

なるべくその時間で集中して勉強するようにしています。また、短期集中レッスンを選んだのはまとめて払うと安くなるという魅力もありますが、1年間通って、ここの教室に対することもそうですが、先生に対する信頼も出来たので、通わせて頂こうという気持ちが強くなりました。それをこなせるかこなせないかはあまり考えてなかったので、なるべくついていこうと思っています。<br><br>

<b>テキストに日本語が全く書かれてないことについてどう思いますか。</b><br>

テキストに日本語が書いてなかったので、逆に韓国語に集中出来る。日本語が書いてあると、日本語に目が行っちゃうし、書かれている英語で多少分かったとしても英語がそんなに上手じゃないので韓国語だけに集中できて、この教科書だから良かったです。また、文法もいい感じにまとまっていたのでいいと思います。
<br><br>
<b>韓訳をしながら復習することは役に立ちましたか。</b><br>

だいぶ役に立ちます。単語だけで覚えるのではなくて文章で覚えてるから、文章の流れが絵的に頭に入って、文も単語だけじゃなくて文章が入ってるから覚える単語も増えてると思うし、先生の声を聴きながらの音読をしてるからリズム、イントネーションも勉強できるから、ドラマを見ても何となく理解できるのが、それで増えたのかなと思います。<br><br>

<b>これからの目標を教えてください。</b><br>

字幕なしで、ドラマを見たいですね。また、一人旅で韓国に行きたいです。韓国に行って本屋さんで本をたくさん買って、韓国の本を読めるくらいのレベルに早くなりたいと思っています。
<br>
(2012年5月、授業終了後収録）
