			                <div class="red">嬉しいことは嬉しいですが、わからないことやできないことが返って多くなったような感じがしています。韓国語の勉強が趣味で、わかるようになることやできるようになることが楽しくて勉強を続けてきたのですが、やればやるほど、まだわからないことやできないことが多いということに気付かされます。今はそのまだわからない、できない部分を、ここまできたからには腰を据えてやっていこうという気持ちです。</div>
			                <br />
			                <strong>Q 今年で何年くらい韓国語を勉強していますか。</strong><br />
			                <div class="red">最初に韓国語に接したのは２００４年です。最初の頃は独学で基本的な発音も文法もわからないまま、それもやったりやらなかったりという状態で７年が過ぎました。やはりきちんと勉強しようと思い、２０１１年の８月からミリネで３級の教科書から始め、４級まで終えました。最初の７年間とミリネでの２年半は勿論比べるのもおかしいとは思いますが、ミリネでの２年半は内容が濃かったです。最初からミリネで習っていたら今頃は韓国語がペラペラになっていたのではないでしょうか？</div>
			                <br />
			                <strong>Q ミリネで4級を勉強されまして、変わったところ、進展があれば教えてください。</strong><br />
			                <div class="red">４級は３級とあまり内容は変わりませんが、さらに細かく様々な表現が出てきました。１課の本文がかなり長くなったので、本文の暗記に時間がかかり大変でした。日中働いているとなかなか時間が取れないので、日本語訳や練習問題などは土日にやり、本文の暗記などは片道３０分の通勤時間に何とかこなしていました。３級の時は１時間でだいたい１課進みましたが、４級は半分くらいしか進みませんでした。<br />
<br />


４級の途中くらいからパダスギを始めました。ハングル日本百科という１５分間のラジオ番組を聞き取って文章にするという何時間もかかる作業です。パダスギは大変ですが効果のある学習方法だと思いました。そのおかげで語彙が増えたと思います。専門的な話題の時は出てくる言葉も専門的なので苦労しましたが、特に漢字語の習得にとても役立ちました。また、濃音化や鼻音化、ニウン挿入などはそういう単語が出てくるたびに教えてくださったので、その決まりがわかるようになり、知らない単語でも、これは鼻音化されているこういう単語かもしれないと見当がつくようになりました。私の発音も前よりよくなったような気がします。（自分で思うだけかもしれませんが。）パダスギはこれからも続けたい学習です。 </div>
			                <br />
			                <strong>Q 一つのテキストが終わると総復習をしますが、はいかがでしたか。</strong> <br />
			                <div class="red">楽しかったです。それまでは予習復習をきちんとやることで１課ずつ細かく表現を覚えたり、練習したりという感じでしたが、総復習は先生が予習はしない方がよいとおっしゃったので、全く準備せず受けたのです。先生が本文の日本語を言われるので、私がそれを韓国語で言うのですが、そこを勉強していた時は「全部覚えた」と思っていたのですが、そんなことはなく忘れてしまっている単語や表現が多かったです。実力テストみたいな感じでした。答えを聞いてみたら「ああ、これなら知っている」という表現が多かったですが、やはり身についた表現でないと実際には使えないのだなあと思いました。でも、私がつかえる度に先生が何度でも丁寧に説明してくださってありがたかったです。総復習に数か月もかかりましたが、その間にだいぶ頭が整理された気がします。</div>
			                <br />
			                <strong>Q ミリネの授業のやり方について。</strong> <br />
			                <div class="red">予習・復習を行えるように進めてくださるのがよいと思います。それと一人一人の生徒の特性や希望に合わせて柔軟に対応されているところもよいと思います。 </div>
			                <br />
			                <strong>Q 今のソウル大学のテキストは日本語がまったく書かれてないですが、4級の勉強にあたって、どうでしたか。</strong> <br />
			                <div class="red">日本語の説明は必要だと思いますが、わからないところは授業の時に聞けるので、特に不便は感じませんでした。「日本語は書いてないから、読むのは大変だけど頑張って読もう」という気持ちにさせてもらいました。何でも楽しては身につかないと思います。 </div>
			                <br />
			                <strong>Q スカイプレッスンの特徴はなんでしょうか。</strong><br />
			                <div class="red">１番のよいところは行き来の時間がかからないというところです。１時間の授業は本当に１時間で終わります。忙しい中の貴重な休日、家にいながら授業が受けられてよかったです。また、教室での授業は先生が黒板に書いて説明してくださいますので、生徒はそれをノートに写しますよね。スカイプでは先生が書かれた（打ってくださった）ものがそのまま残るので、後で復習するときにもそれを見ながらできます。先生がノートを作ってくださっている感じです。</div>
			                <br />
                            
                            
                             <strong>Q これからは5級のテキストで勉強されると思いますが、目標を教えてください。</strong><br />
			                <div class="red">「自然な韓国語を使えるようになること」と「ドラマを字幕なしで見ること」が目標です。どちらもちょっとやったくらいでできるようになるものでないことをこれまでの学習で実感しましたが、今は自由になる時間が増え、前よりもたくさん勉強できそうな気がしますので頑張ってみようと思います。今やっているパダスギを続けること、小説を読むこと、会話をすること、音読、日記などを頑張っていきたいです。毎日コツコツ積み重ねることで、少しずつでも進歩していけたらなあと思います。</div>
			                <br />
                            <hr>
                               <h5 class="date">  Sさん(1級・北区在住)</h5>

<p> <strong>Q１級テキストを終えたご感想は</strong>。</p>

<div class="red">やっと1冊終わった、まだまだ先は長い、という感じです。</div>

<p> <strong>Qミリネで勉強されまして、変わったところ、進展があれば教えてください。</strong></p>

<div class="red">今までは独学勉強を始めてはすぐやめて、の繰り返しで全く進展がありませんでしたが、ミリネで勉強を始めてから少しずつしゃべれるようになってきたと思います。また、ラジオでも雑誌でも、意味がとれる部分が少しずつ出てきたと思います。</div>

<p> <strong>Q韓国語を習い始めたきっかけは。 </strong> </p>

<div class="red">

2002年に韓国の音楽に出会って、  勉強しようしようと思いながら10年以上が過ぎてしまいました。去年からあるK-popグループを好きになり、インタビューや番組での発言をもっと理解したいと思い、ちょうどお金にも少し余裕が出てきたので習い始めました。</div><br>

<p> <strong>Qミリネで勉強をしようと思ったきっかけは</strong>。</p>

<div class="red">

インターネットで山手線沿線の韓国語教室を調べていて見つけました。

最初に無料体験を受けて、みっちりやれそうだったので決めました。</div>
			                <br />

<p> <strong>Qミリネの授業のやり方について。</strong></p>

<div class="red">

教科書の内容に即して、自由に話を振ってくれるところがよいと思います。教科書の内容だけだと少し単調になりそうですが、たまに話が脱線したりしながら関連語句・表現をいろいろ教えてくださるので、自分が言いたいことも少しずつ言えるようになるし、楽しいです。</div>
			                <br />

<p> <strong>Q今のソウル大学のテキストは日本語がまったく書かれてないですが、どうでしたか</strong>。</p>

<div class="red">

一文ずつ訳をするときに英語を見てしまうときもありますが、日本語が目に入ることがないのがいいと思います。</div>
			                <br />

<p> <strong>Qこれからの目標を教えてください。</strong></p>

<div class="red">

単語や活用、表現をきちんと覚えて、もっと自然に話ができるようになりたいです。あと、今年は作文力をつけることをやっていきたいです。<br>

(2013年9月）</div>
			                <br />
                            <hr>
                         <h5 class="date">  Kさん　（4級、東京在住）</h5>


			                <br />

<p> <strong>Q① 3級テキストを終えたご感想は</strong>。</p>

<div class="red">

今この1年間3級の教科書をなめるように学習したと感じます。

ミリネでは当たり前ですが、本文及び文法と表現のところは全て韓訳できるまで理解し、体得させます。その方法としてのシャドーイングがなかなか出来なくて苦労しましたが、声を出して読み込むことと聞くことの繰り返しによって出来るんだとわかり、楽しくなってきました。

どんな些細なことを質問しても詳しく理解するまで教えてくださり、うまくできなかった日も励まし続けてくださった先生のおかげです。本当に有難うございました。 

</div>
			                <br />

<p> <strong>Q②  ミリネで勉強をしようと思ったきっかけは</strong>。</p>

<div class="red">

「はなせるミリネ韓国語教室」のホームページを拝見して、ここに来ればはなせるようになると直感しました。つまり韓国語を話して覚え、覚えて話したかったんです。そして授業料も財布にやさしい価格でした。

それまでは他の学校のグループレッスンを受けていましたが、授業中に発言する時間が短いし予習もおおむね文法を理解することに費やしていました。

もっと早くミリネ韓国語教室を知っていたら良かったと思います。

</div>
			                <br />

<p> <strong>Q③  ミリネで勉強されてから変わったところ、進展があれば教えてください。</strong></p>

<div class="red">

ズバリ発音です。習い始めた日の教科書には緑のマーカーで（発音の注意を受けた所）25か所線を引いてありました。いかに発音を逐次直していただいたかわかりますよね。読むのも自信が持てないし、聞き取れないし、話すのも自信が持てないという状態でした。間違うと必ずきちんと納得するまで直してくださるので、安心して音読を続けられます。またボイスレコーダーの授業中の使用を容認してもらい、自宅に帰ってもどこがどう違っていたのかが確認できてとても助かりました。

</div>
			                <br />

<p> <strong>Q④  ミリネの授業のやり方について、どう思いますか</strong>。</p>

<div class="red">

授業では70～80％ぐらい生徒が声を出して何か言っています。そのためには予習もそれ以上に声に出して言いながら学習せざるを得ません。

やはり声に出して言い、また聞いて理解するというサイクルが本当に大事なことだと思いました。また韓国で韓国語を習うときには当然日本語訳などさせられませんでしたが、これが意外に大事で、なんとなくいい加減に理解した所がこれでバレるんです。

こういう基本的だけど大事なことをコツコツと学習をしなくてはいけないミリネの授業はいいと思います。

</div>
			                <br />

<p> <strong>Q⑤  韓訳をしながら復習することは役に立ちましたか</strong>。</p>

<div class="red">

3級のテキストを一通り終了してから、再度1課から韓訳の復習をしました。（他の学校では聞いたことのないことです）教えっぱなしではなく、理解しているか確認し、最後まで責任をもって教えていただいたと実感しました。韓訳で引っかかったところを、書き取りをしてみたり、文法を確認したり、また質問を繰り返したりして、理解が深まりました。

</div>
			                <br />

<p> <strong>Q⑥  今のソウル大学のテキストは日本語がまったく書かれてないですが、どうでしたか。</strong></p>

<div class="red">

教科書には日本語がなくても、何の問題もありませんでした。授業で文章を日本語にして確認をしますし、韓訳もして理解を深めますから。また文法的にわからなければ、わかるまで何度でも教えてくださるし、かえって日本語によるわかりづらい説明がないほうがいいです。

</div>
			                <br />

<p> <strong>Q⑦  韓国語を習い始めたきっかけは。</strong></p>

<div class="red">

　留学をするためでした。また韓国で韓国料理も習いたいと思いました。

</div>
			                <br />

<p> <strong>Q⑧  これからの目標を教えてください。</strong></p>
<div class="red">
まずは今している学習をつづけること。そして韓国の小学校低学年程度の本をすらすら音読できるようになりたいし、韓国料理もハングルレシピを読んで、沢山作ってみたいと思います。日常の中にハングルのある生活をすることです。

また機会をとらえて韓国で生活したいと思っています。
<br></div>
(2012年11月）

