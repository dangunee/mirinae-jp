function buildRows(themes: string[]): CurriculumRow[] {
  const rows: CurriculumRow[] = [];
  for (let i = 0; i < 12; i++) {
    rows.push({
      koma: String(i + 1),
      c12: themes[i] || "",
      c24: themes[i] || "",
      c48: themes[i] || "",
      theme12: "kaiwa",
      theme24: "kaiwa",
      theme48: "kaiwa",
    });
  }
  rows.push({
    koma: "13",
    c12: "12コマ修了",
    c24: "",
    c48: "",
    theme12: "end",
    theme24: "",
    theme48: "",
  });
  for (let i = 12; i < 24; i++) {
    rows.push({
      koma: String(i + 1),
      c12: "",
      c24: themes[i] || "",
      c48: themes[i] || "",
      theme12: "",
      theme24: "kaiwa",
      theme48: "kaiwa",
    });
  }
  rows.push({
    koma: "25",
    c12: "",
    c24: "24コマコース修了",
    c48: "",
    theme12: "",
    theme24: "end",
    theme48: "",
  });
  for (let i = 24; i < 48; i++) {
    rows.push({
      koma: String(i + 1),
      c12: "",
      c24: "",
      c48: themes[i] || "",
      theme12: "",
      theme24: "",
      theme48: "kaiwa",
    });
  }
  rows.push({
    koma: "49",
    c12: "",
    c24: "",
    c48: "48コマコース修了",
    theme12: "",
    theme24: "",
    theme48: "end",
  });
  return rows;
}

const CURRICULUM_BLOCKS = [
  { blockKey: "curriculum_shokyu", title: "初級", rows: buildRows(SHOKYU_THEMES) },
  { blockKey: "curriculum_chukyu", title: "中級", rows: buildRows(CHUKYU_THEMES) },
  { blockKey: "curriculum_jokyu", title: "上級", rows: buildRows(JOKYU_THEMES) },
];

export async function POST() {
  try {
    for (const block of CURRICULUM_BLOCKS) {
      await prisma.siteTable.upsert({
        where: { pageSlug_blockKey: { pageSlug: PAGE_SLUG, blockKey: block.blockKey } },
        create: {
          pageSlug: PAGE_SLUG,
          blockKey: block.blockKey,
          title: block.title,
          rowsJson: JSON.stringify(block.rows),
        },
        update: {
          title: block.title,
          rowsJson: JSON.stringify(block.rows),
        },
      });
    }
    return NextResponse.json({
      ok: true,
      blocks: CURRICULUM_BLOCKS.map((b) => ({ blockKey: b.blockKey, title: b.title, rows: b.rows.length })),
    });
  } catch (e) {
    console.error(e);
    return NextResponse.json({ error: String(e) }, { status: 500 });
  }
}
